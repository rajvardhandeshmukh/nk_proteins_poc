def call_ai_provider_orchestration(user_prompt, system_prompt, model_name=DEFAULT_LLM_MODEL, provider=DEFAULT_PROVIDER):
    """
    Calls the AI Provider Orchestration API.
    """
    # Read from environment each time to handle late .env additions
    env_path = os.path.join(os.path.dirname(__file__), '.env')
    load_dotenv(dotenv_path=env_path)
    api_url = os.environ.get("AI_PROVIDER_ORCHESTRATION_API_URL", "")
    api_key = os.environ.get("AI_PROVIDER_ORCHESTRATION_API_KEY", "")

    if not api_url or not api_key:
        return {"error": "Missing API configuration in .env"}
    
    headers = {
        "Content-Type": "application/json",
        "Authorization": f"Bearer {api_key}"
    }
    
    payload = {
        "model": model_name,
        "provider": provider,
        "prompts": [system_prompt, user_prompt]
    }
    
    try:
        # Construct the URL, ensuring no double slashes
        base_url = api_url.rstrip('/')
        target_url = f"{base_url}/ai/generate"
        response = requests.post(target_url, headers=headers, json=payload, timeout=30)
        
        # Log successful (or status-error) response
        log_interaction(payload, {"status": response.status_code, "text": response.text})
        
        response.raise_for_status()
        return response.json()
    except Exception as e:
        # Log exception
        log_interaction(payload, None, error=str(e))
        return {"error": str(e)}

