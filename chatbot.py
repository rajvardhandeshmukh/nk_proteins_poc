class DateTimeEncoder(json.JSONEncoder):
    """Custom JSON encoder to handle pandas Timestamps and datetimes."""
    def default(self, obj):
        if hasattr(obj, 'strftime'):
            return obj.strftime('%Y-%m-%d')
        return super().default(obj)

